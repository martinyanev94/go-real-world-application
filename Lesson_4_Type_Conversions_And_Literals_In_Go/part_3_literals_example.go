package main

import "fmt"

func main() {
    var integerLiteral int = 100                       // Integer literal
    var floatLiteral float64 = 25.50                   // Floating-point literal
    var booleanLiteral bool = true                      // Boolean literal
    var stringLiteral string = "Hello, Go!"            // String literal
    var runeLiteral rune = 'G'                          // Rune literal

    fmt.Println("Integer Literal:", integerLiteral)
    fmt.Println("Float Literal:", floatLiteral)
    fmt.Println("Boolean Literal:", booleanLiteral)
    fmt.Println("String Literal:", stringLiteral)
    fmt.Println("Rune Literal:", runeLiteral)
}
