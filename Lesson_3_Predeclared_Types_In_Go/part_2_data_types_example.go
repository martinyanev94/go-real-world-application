package main

import "fmt"

func main() {
    var a int = 10          // signed integer
    var b uint = 20        // unsigned integer
    var c float32 = 3.14   // floating-point number
    var d float64 = 2.718  // double precision floating-point number

    fmt.Println("Signed Integer:", a)
    fmt.Println("Unsigned Integer:", b)
    fmt.Println("Float32 Value:", c)
    fmt.Println("Float64 Value:", d)
}
